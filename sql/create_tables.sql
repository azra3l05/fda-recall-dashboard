CREATE TABLE IF NOT EXISTS recalls (
            recall_number TEXT,
            product_type TEXT,
            recalling_firm TEXT,
            classification TEXT,
            city TEXT,
            state TEXT,
            country TEXT,
            reason_for_recall TEXT,
            status TEXT,
            product_description TEXT,
            code_info TEXT,
            distribution_pattern TEXT,
            report_date DATE,
            recall_initiation_date DATE,
            event_id TEXT,
            termination_date DATE
)